class Skill:

    def __init__(self, nome, categoria, punteggio, gia):
        self.nome = nome
        self.categoria = categoria
        self.punteggio = punteggio
        self.gia = gi

    def __repr__(self):
        return f"Skill(nome='{self.nome}', categoria='{self.categoria}', punteggio={self.punteggio})"






